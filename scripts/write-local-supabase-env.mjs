#!/usr/bin/env node
/**
 * Write `.env` from a running local Supabase stack (`supabase start`).
 * Maps CLI status env vars to Vite `VITE_SUPABASE_*` keys.
 *
 * Usage: npm run supabase:env
 */
import { spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";

const result = spawnSync("npx", ["supabase", "status", "-o", "env"], {
  encoding: "utf8",
  shell: process.platform === "win32",
});

if (result.status !== 0) {
  console.error(
    result.stderr ||
      result.stdout ||
      "supabase status failed. Run `npm run supabase:start` first."
  );
  process.exit(result.status ?? 1);
}

const env = Object.fromEntries(
  result.stdout
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const i = line.indexOf("=");
      if (i === -1) return null;
      const key = line.slice(0, i);
      let value = line.slice(i + 1);
      // status -o env may quote values
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1);
      }
      return [key, value];
    })
    .filter(Boolean)
);

const url = env.API_URL || env.SUPABASE_URL;
const anon =
  env.ANON_KEY ||
  env.PUBLISHABLE_KEY ||
  env.SUPABASE_ANON_KEY ||
  env.SUPABASE_PUBLISHABLE_KEY;

if (!url || !anon) {
  console.error(
    "Could not find API_URL / ANON_KEY (or publishable key) in `supabase status -o env`."
  );
  console.error("Raw output:\n", result.stdout);
  process.exit(1);
}

const contents = `# Generated by npm run supabase:env — local Supabase stack
# Studio: see \`npm run supabase:status\`
VITE_SUPABASE_URL=${url}
VITE_SUPABASE_ANON_KEY=${anon}
VITE_DEBUG=false
`;

const out = resolve(process.cwd(), ".env");
writeFileSync(out, contents, "utf8");
console.log(`Wrote ${out}`);
console.log(`VITE_SUPABASE_URL=${url}`);
console.log(`VITE_SUPABASE_ANON_KEY=${anon.slice(0, 12)}…`);
